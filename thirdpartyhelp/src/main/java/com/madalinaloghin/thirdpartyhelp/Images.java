public class Images {
    private String mName;
    private String imageUrl;

    public Images(String mName, String imageUrl) {
        this.mName = mName;
        this.imageUrl = imageUrl;
    }

    public String getmName() {
        return mName;
    }

    public String getImageUrl() {
        return imageUrl;
    }
}
